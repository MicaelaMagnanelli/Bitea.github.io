// Función para obtener el parámetro 'lang' de la URL
function getLanguageFromURL() {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get('lang') || null; // Devuelve null si no hay parámetro
}

// Función para actualizar el contenido según el idioma
function updateLanguage() {
    // Obtener el idioma de la URL o de localStorage
    let language = getLanguageFromURL();
    
    // Si no hay idioma en la URL, intentar obtenerlo de localStorage
    if (!language) {
        language = localStorage.getItem('language') || 'es';
    } else {
        // Guardar el idioma en localStorage para futuras visitas
        localStorage.setItem('language', language);
    }

    // Asegurarse de que el idioma sea válido ('es' o 'en')
    language = ['es', 'en'].includes(language) ? language : 'es';

    // Actualizar el contenido de la página
    const texts = translations[language] || translations['es'];
    Object.keys(texts).forEach(id => {
        const element = document.getElementById(id);
        if (element) {
            element.textContent = texts[id];
        }
    });

    // Actualizar el atributo lang del HTML para accesibilidad
    document.documentElement.lang = language;
}

// Ejecutar la función al cargar la página
window.addEventListener('DOMContentLoaded', updateLanguage);